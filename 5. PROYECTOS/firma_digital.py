"""# FIRMAR EL DOCUMENTO



1.   Se comprueba la integridad del documento
2.   Se comprueba la identidad del firmante


"""

from Crypto.Hash import SHA
from Crypto.Signature import PKCS1_v1_5 as Signature_pkcs1_v1_5
from Crypto.PublicKey import RSA
import base64

def verificar(message, signature):
    with open('ClavePublicaGXOElux.pem') as f:
        key = f.read()
        rsakey = RSA.importKey(key)
        verifier = Signature_pkcs1_v1_5.new(rsakey)
        digest = SHA.new()
        
        # Asumimos que los datos estan codificados en base64
        digest.update(message)
        print("Hash del documento recibido: ", digest.hexdigest())
        print("Desencriptamos la firma para extraer el hash original")
        is_verify = verifier.verify(digest, base64.b64decode(signature))

    #devolvemos la respuesta

    if is_verify:
        print("✅ OK, los hashes coinciden. La firma del documento es valida")
        print("✅ El documento no ha sido alterado y su autor es quien dice ser")
    else:
        print("❌ La firma del documento no es correcta")
        print("❌ El documento o la firma han sido alterados o el firmante no es correcto")

