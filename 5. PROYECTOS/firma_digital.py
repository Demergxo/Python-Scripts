"""# FIRMAR UN DOCUMENTO


1.   Creamos un hash cobre el contenido del documento
2.   Encriptamos el hash con nuestra clave privada
3.   El resultado es la firma digital




"""

from Crypto.Hash import SHA
from Crypto.Signature import PKCS1_v1_5 as Signature_pkcs1_v1_5
from Crypto.PublicKey import RSA
import base64

# Usar clave para firmar el archivo

def firma(message):
    with open ('ClavePrivadaGXOElux.pem') as f:
        key= f.read()
        rsakey = RSA.importKey(key)
        signer = Signature_pkcs1_v1_5.new(rsakey)

        #Cifrado HASH
        
        digest = SHA.new()
        
        #Firmamos
        
        digest.update(message)
        print("🔓 Contenido del documento: ", message)
        print("🔓 Calculamos hash del documento: ", digest.hexdigest())

        sign = signer.sign(digest)
        signature = base64.b64encode(sign)
            
    with open ('firma.pem', 'wb') as fp1:
        fp1.write(signature)
        fp1.close()
    print("🤟 Firma Creada", signature)
    print("🤟 Firma Guardada en : firma.pem")

    return signature

